package com.DueniosyMascotas.pruebaDuenioMascotas.model;

public class Duenio {
	private Integer dni;
	private String nombre;
	

	public Integer getDni() {
		return dni;
	}

	public void setDni(Integer dni) {
		this.dni = dni;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
}
